
# Relational Operaters this compares value and returns boolean values

a = 20
b = 10
   
# a > b is greater than
print(a > b) 
   
# a < b is lesser than
print(a < b) 
   
# a == b is logical equal to
print(a == b) 
   
# a != b is not equal
print(a != b) 
   
# a >= b is  greater than or equal to
print(a >= b) 
   
# a <= b is  lesser than or equal to
print(a <= b)