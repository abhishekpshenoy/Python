def add(num1, num2):
    """Addition function

    Give two inputs and take one output which is the sum of each input
    """
    num1 = float(num1)
    num2 = float(num2)
    return num1 + num2

def sub(num1, num2):
    """Substraction Function

    Give two inputs and the result will be num1 - num2
    """
    num1 = float(num1)
    num2 = float(num2)
    return num1-num2
